      <h2 class="text-base leading-6 font-medium text-grey-900 mb-2">How does access work?</h2>
      <p class="text-sm mb-2 text-grey-900">Access to the portal is by invite only. Once invited, teachers and their students will access the portal using a secure sign in method:</p>
      <ul class="text-sm mb-4 text-grey-900">
        <li class="text-sm mb-1 text-grey-900">1. They will enter the email address associated with their account.</li>
        <li class="text-sm mb-1 text-grey-900">2. They will be sent an email containing a 6-digit code.</li>
        <li class="text-sm mb-1 text-grey-900">3. They will enter their 6-digit code as a single-use password.</li>
      </ul>
      <p class="text-sm mb-4 text-grey-900">They will need to do this <span class="font-bold">every time they sign in</span>.</p>
      <div class="bg-yellow-100 border border-yellow-500 px-4 py-2 mb-8 rounded">
        <p class="text-sm text-yellow-700 mb-2">Please note this single-use password expires 10 minutes after being issued. We plan to shorten this duration in future.</p>
        <p class="text-sm text-yellow-700 text-medium">This sign in method is much more secure than using a standard password and is fast becoming a standard across the web.</p>
      </div>

      <h2 class="text-base leading-6 font-medium text-grey-900 mb-2">Solutions to common problems</h2>
      <h3 class="text-sm mb-1 text-grey-900 font-medium italic">How do I sign in?</h3>
      <ul class="text-sm mb-4 text-grey-900">
        <li class="text-sm mb-1 text-grey-900">- Return to <a href="/" target="_blank" class="underline text-blue-600 font-medium">the signin page</a> and select the <strong>Email a sign in code</strong> option.</li>
        <li class="text-sm mb-1 text-grey-900">- Enter your email address and press <strong>Send me a sign in code</strong>.</li>
        <li class="text-sm mb-1 text-grey-900">- Check your email and wait for an email from an address ending in @leiths.com. It will contain a 6-digit code.</li>
        <li class="text-sm mb-1 text-grey-900">- Enter the 6-digit code and press <strong>Sign in</strong>.</li>
      </ul>
      
      <h3 class="text-sm mb-1 text-grey-900 font-medium italic">I've entered my email address but I never received an email.</h3>
      <p class="text-sm mb-4 text-grey-900">Emails normally arrive within seconds. Check your Spam/Junk folder. If the email isn't in there, students should contact their teacher; teachers should contact their <span class="underline">school's IT team</span> - sending them a link to this page.</p>
      
      <h3 class="text-sm mb-1 text-grey-900 font-medium italic">When entering my email address, I receive the error "Signups not allowed for this instance".</h3>
      <p class="text-sm mb-4 text-grey-900">This means you have attempted to sign in using an email address the system does not recognise. Reload the page and enter the email address you were invited with. If you keep receiving this error: students should confirm their email address with their teacher; teachers should contact Leiths Academy directly.</p>
      
      <h3 class="text-sm mb-1 text-grey-900 font-medium italic">When entering the 6-digit code from my email, I receive the error "Code  expired or invalid".</h3>
      <p class="text-sm mb-4 text-grey-900">You have either entered the code incorrectly or it has expired. Reload the page and request a new code. For security reasons, the codes have short lifespans.</p>
      
      <h3 class="text-sm mb-1 text-grey-900 font-medium">For any other issues...</h3>
      <p class="text-sm mb-2 text-grey-900">As with any software, you may encounter issues from time to time. Students should report these to their teacher; teachers, please report them directly to Leiths Academy. The more information you provide, the quicker we can work to resolve it - please provide a detailed description, a date and time, and screenshots if you can.</p>

